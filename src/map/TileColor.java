package map;

public class TileColor {
    public final int r;
    public final int g;
    public final int b;
    public final int tileNumber;

    public TileColor(int r, int g, int b, int tileNumber) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.tileNumber = tileNumber;
    }
}